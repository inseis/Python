# 2. 데이터 더 알아보기
# 2-1. 문자열
# 포맷팅: 문자열의 중간에 원하는 변수를 대입할 수 있다
print(f"{name1}은 {age1}살이고 {gender1}입니다")
print(f"{name2}은 {age2}살이고 {gender2}입니다")

# 2-2. dictionary
# 연관성 있는 데이터끼리는 dictionary에 잘 응집시켜서 관리하자
# dictionary = {키: 값}
user1 = {
    "name": "김동그라미",
    "age": 21,
    "gender": "여성",
}
user2 = {
    "name": "김네모",
    "age": 25,
    "gender": "남성",
}
print(f"{user1.get('name')}은 {user1.get('age')}살이고 {user1.get('gender')}입니다")
print(f"{user2.get('name')}은 {user2.get('age')}살이고 {user2.get('gender')}입니다")

# 2-3. list
# 데이터가 많아지면 list에 담아서 관리하자
# list = [원소]
users = [user1, user2]
print(f"{users[0].get('name')}은 {users[0].get('age')}살이고 {users[0].get('gender')}입니다")
print(f"{users[1].get('name')}은 {users[1].get('age')}살이고 {users[1].get('gender')}입니다")
user3 = {
    "name": "김세모",
    "age": 29,
    "gender": "남성",
}
users.append(user3)
print(f"{users[2].get('name')}은 {users[2].get('age')}살이고 {users[2].get('gender')}입니다")

