# 4. 함수
# 반복적인 일이 존재한다면 함수로 묶어서 관라한다
# 함수는 직원을 고용하는 것과 같다
# 함수라는 직원이 사용할 데이터를 주고, 결과를 받아서 사용하자

# def 함수_이름(함수에서 사용할 데이터):
#     데이터를 사용해서 결과를 계산
#     return 결과
def decide_student_state(user):
    if user.get('gender') == "남성":
        if user.get('age') <= 25:
            return True
        else:
            return False
    else:
        if user.get('age') <= 23:
            return True
        else:
            return False
